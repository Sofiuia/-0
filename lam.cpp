#include <iostream>

// Функтор - це клас, який перевантажує оператор () 
//і може бути викликаний, як функція. 

class Functor {
public:
    void operator()(int x) {
        std::cout << "Функтор викликано з параметром: " << x << std::endl;
    }
};

int main() {
    Functor f;  // Створення об'єкта функтора
    f(42);      // Виклик функтора, аналогічно функції

    return 0;
}
